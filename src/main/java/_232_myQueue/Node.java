package _232_myQueue;

public class Node {
    int value;
    Node next;

    Node(int x) {
        this.value = x;
        this.next = null;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

}
